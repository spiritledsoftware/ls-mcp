#!/usr/bin/env node
import { access, mkdir, readFile, rm, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { spawnSync } from "node:child_process";

const root = resolve(import.meta.dirname, "..");
const outputDir = resolve(root, "src/tools/generated");

const tasks = [
  {
    input: await firstExistingPath([
      "node_modules/vscode-languageserver-types/lib/esm/main.d.ts",
      "node_modules/vscode-languageserver-types/lib/umd/main.d.ts",
    ]),
    output: "src/tools/generated/lspTypes.ts",
    header:
      "// Generated from vscode-languageserver-types by scripts/generate-lsp-output-schemas.mjs. Do not edit by hand.",
  },
  {
    input:
      "node_modules/vscode-languageserver-protocol/lib/common/protocol.linkedEditingRange.d.ts",
    output: "src/tools/generated/lspLinkedEditingRange.ts",
    header:
      "// Generated from vscode-languageserver-protocol protocol.linkedEditingRange by scripts/generate-lsp-output-schemas.mjs. Do not edit by hand.",
  },
  {
    input: "node_modules/vscode-languageserver-protocol/lib/common/protocol.moniker.d.ts",
    output: "src/tools/generated/lspMoniker.ts",
    header:
      "// Generated from vscode-languageserver-protocol protocol.moniker by scripts/generate-lsp-output-schemas.mjs. Do not edit by hand.",
  },
];

async function firstExistingPath(paths) {
  for (const path of paths) {
    try {
      await access(resolve(root, path));
      return path;
    } catch {
      // Try the next published layout.
    }
  }
  throw new Error(`None of these schema source files exist: ${paths.join(", ")}`);
}

await rm(outputDir, { recursive: true, force: true });
await mkdir(outputDir, { recursive: true });

for (const task of tasks) {
  const result = spawnSync(
    "pnpm",
    ["ts-to-zod", task.input, task.output, "--skipValidation", "--skipParseJSDoc"],
    { cwd: root, encoding: "utf8", stdio: "inherit" },
  );
  if (result.status !== 0) {
    process.exit(result.status ?? 1);
  }
  await normalizeGeneratedFile(resolve(root, task.output), task.header);
}

const format = spawnSync("pnpm", ["oxfmt", "src/tools/generated"], {
  cwd: root,
  encoding: "utf8",
  stdio: "inherit",
});
if (format.status !== 0) {
  process.exit(format.status ?? 1);
}

async function normalizeGeneratedFile(filePath, header) {
  const source = await readFile(filePath, "utf8");
  const normalized = source
    .replace(/^\/\/ Generated by ts-to-zod\n/, `${header}\n`)
    .replace(/^import \{[^\n]+\} from "\.\/\.\.\/\.\.\/\.\.\/node_modules\/[^\n]+\n/gm, "")
    .replace(/^import \{[^\n]+\} from "\.\/\.\.\/\.\.\/node_modules\/[^\n]+\n/gm, "")
    .replace(/^import \{[^\n]+\} from "\.\/\.\.\/node_modules\/[^\n]+\n/gm, "")
    .replace(/^import \{[^\n]+\} from "\.\/node_modules\/[^\n]+\n/gm, "")
    .replace(/: z\.ZodSchema<[^>]+>/g, ": z.ZodType")
    .replace(
      /^export declare const semanticTokenTypesSchema = z\.enum\(SemanticTokenTypes\);$/gm,
      "export const semanticTokenTypesSchema = z.string();",
    )
    .replace(
      /^export declare const semanticTokenModifiersSchema = z\.enum\(SemanticTokenModifiers\);$/gm,
      "export const semanticTokenModifiersSchema = z.string();",
    )
    .replace(/^const requestHandlerSchema = z\.any\(\);\n\n/gm, "")
    .replace(/^const requestHandlerSchema = z\.object\(\{\}\)\.passthrough\(\);\n\n/gm, "")
    .replace(/^const \w+RequestHandlerSignatureSchema = requestHandlerSchema;\n?/gm, "")
    .replace(/^const (\w+Schema) = z\.any\(\);$/gm, "const $1 = z.looseObject({});")
    .replace(/z\.object\(\{/g, "z.looseObject({")
    .replace(/\r\n/g, "\n");
  await mkdir(dirname(filePath), { recursive: true });
  await writeFile(filePath, normalized, "utf8");
}
